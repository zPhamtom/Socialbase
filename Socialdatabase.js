function mostrarPorDepartamento(departamento) {
  const div = document.getElementById("resultado");
  div.innerHTML = "";

  if (departamento === "") {
    div.innerHTML =
      "<p style='color:white;text-align:center;'>Selecione um departamento.</p>";
    return;
  }

  const listaFiltrada = listaA.filter((u) => u.departamento === departamento);

  if (listaFiltrada.length === 0) {
    div.innerHTML =
      "<p style='color:white;text-align:center;'>Nenhum usuário encontrado.</p>";
    return;
  }

  listaFiltrada.forEach((user) => criarCard(user));
}

function mostrarPorEmail(emailDigitado) {
  const div = document.getElementById("resultado");
  div.innerHTML = "";

  if (emailDigitado === "") {
    div.innerHTML =
      "<p style='color:white;text-align:center;'>Digite um email para pesquisar.</p>";
    return;
  }

  const filtrado = listaA.filter((u) =>
    u.email.toLowerCase().includes(emailDigitado)
  );

  if (filtrado.length === 0) {
    div.innerHTML =
      "<p style='color:white;text-align:center;'>Nenhum email encontrado.</p>";
    return;
  }

  filtrado.forEach((user) => criarCard(user));
}

function criarCard(user) {
  const div = document.getElementById("resultado");

  const ativo = ativosSet.has(user.email.trim().toLowerCase())
    ? "Ativo no Jira"
    : "Inativo no Jira";

  const card = document.createElement("div");
  card.style.background = "#675fed";
  card.style.color = "white"; // nessa parte utilizei IA nesses cards
  card.style.width = "400px";
  card.style.margin = "10px auto";
  card.style.padding = "12px";
  card.style.borderRadius = "10px";
  card.style.fontSize = "16px";
  card.style.fontWeight = "bold";
  card.style.textAlign = "center";

  card.textContent = `${user.nome} — ${user.email} — ${ativo}`;

  div.appendChild(card);
}
